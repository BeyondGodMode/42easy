#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac == 1)
		return (0);
	i = ac - 1;
	while (i > 0)
	{
		while (*av[i] != '\0')
		{
			write(1, av[i], 1);
			av[i]++;
		}
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
